				<p>At the <u>AvantFarm</u> we produce essential nutrition, primarily for ourselves, the community of the B²COOP. We seek in every way to mimic nature. We designed a holistic food web connecting independent, but mutually beneficial cycles of aquaponics, plants, mushrooms, and insects. We wholeheartedly support aquaponics as a source of nutrients rather than any synthetic solution. Aquaponics allows a truly circular model of production through which we manage to capture, supply, and recirculate nutrients for our ecosystem while creating value in all its stages.</p>
				<p>The <u>AvantMarket</u> is where the harvest arrives to supply essential healthy food to the citizens of the Bajes Kwartier. The harvest comes from the AvantGarden, but not exclusively. Some products, such as wheat, can be produced in our system but make much more sense to grow outdoors at a different scale. Therefore, we seek to cooperate for high-nutritional produce, sourced locally and responsibly, rather than compete and disrupt the local business environment. The AvantMarket is designed as a farmer’s market held year round. Each stand in the AvantMarket tells the story of the farmer. B²COOP provides the space for the stand and B²COOPmembers-employees work as the cashiers, stock managers and cleaners of the market.</p>
