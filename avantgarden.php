		<div class="grid_4-content">
			<h4 class="section-subtitle">We borrow our overarching philosophy from permaculture, where cooperation is the basis of existing life systems and future survival.</h4>
			<div class="section-text">
				<p>With that in mind, we have designed a multi-stakeholder cooperative B²COOP: a cooperative of citizens living in the Bijlmer Bajes (B²), the AvantGardeners, other local farmers and small businesses, working together to build lasting relationships based upon fair prices and wages, and forming community in a common effort to provide the essentials for a healthy, fulfilling life.</p>
				<p>B²COOP is the legal organization that embodies the ethical principles guiding the succession to a resilient community around the AvantGarden. By investing into B²COOP, you invest directly into your community, lifestyle and neighborhood prosperity. In return you receive healthy food, and the opportunity to be apart of something bigger than yourself. All of the profits of B²COOP are reinvested back into the AvantGarden mission, supporting community development and slowly bringing change to the way urban areas feed themselves.</p>
				<p>Around the world we see a monoculture of food production, but also of culture. To avoid this, we have devised a local currency, B²Coin. Each of us in B²COOP can either invest money or time into the community, and running of the AvantGarden. There are many opportunities to obtain ownership through work, all creating social value, increasing employment, and lowering costs. B²Coin circulates within the kwartier; the money you pay employs your neighbor who then buys your crafts, and so on.</p>
